using System;

namespace game
{
    class Program
    {
        static void Main(string[] args)
        {
            Console.Write("What is your grade? ")
            
            string grade = ReadLine()

            if float({grade} >= 90 )
            {
                Console.WriteLine("You made an A.")
            }
            elif float({grade} >= 80 && <90 )
            {
                Console.WriteLine("You made an B.")
            }
            elif float({grade} >= 70 && <80 )
            {
                Console.WriteLine("You made an C.")
            }
            elif float({grade} >= 60 && <70 )
            {
                Console.WriteLine("You made an D.")
            }
            else float({grade} <60)
            {
                Console.WriteLine("You made an F.")
            }
        }
    }
}